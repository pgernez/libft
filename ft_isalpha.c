#include "libft.h"

int	ft_isalpha(int c)
{
	unsigned char	a;

	a = (unsigned char)c;
	if ((a >= 'A' && a <= 'Z') || (a >= 'a' && a <= 'z'))
		return (1);
	else
		return (0);
}
